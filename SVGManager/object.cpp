#include "object.h"

Object::Object() :
	id(INVALID_ID),
	type(ObjectType::INVALID_OBJECT),
	xCoord(0.f),
	yCoord(0.f)
{

}

//*************************************************************************************************************
//*************************************************************************************************************

Object::~Object() {

}

//*************************************************************************************************************
//*************************************************************************************************************

int Object::getId() const {
	return id;
}

//*************************************************************************************************************
//*************************************************************************************************************

ObjectType Object::getType() const {
	return type;
}

//*************************************************************************************************************
//*************************************************************************************************************

float Object::getXCoord() const {
	return xCoord;
}

//*************************************************************************************************************
//*************************************************************************************************************

float Object::getYCoord() const {
	return yCoord;
}

//*************************************************************************************************************
//*************************************************************************************************************

void Object::setId(int id) {
	this->id = id;
}

//*************************************************************************************************************
//*************************************************************************************************************

void Object::setType(ObjectType type) {
	this->type = type;
}

//*************************************************************************************************************
//*************************************************************************************************************

void Object::setXCoord(float xCoord) {
	this->xCoord = xCoord;
}

//*************************************************************************************************************
//*************************************************************************************************************

void Object::setYCoord(float yCoord) {
	this->yCoord = yCoord;
}
