#include "game_data.h"

using namespace std;

GameData::GameData() :
	turns(0)
{

}

//*************************************************************************************************************
//*************************************************************************************************************

GameData::~GameData() {
	turns.clear();
}

//*************************************************************************************************************
//*************************************************************************************************************

float GameData::getWorldWidth() const {
	return worldWidth;
}

//*************************************************************************************************************
//*************************************************************************************************************

float GameData::getWorldHeight() const {
	return worldHeight;
}

//*************************************************************************************************************
//*************************************************************************************************************

string GameData::getBackgroundColor() const {
	return backgroundColor;
}

//*************************************************************************************************************
//*************************************************************************************************************

void GameData::setWorldWidth(float worldWidth) {
	this->worldWidth = worldWidth;
}

//*************************************************************************************************************
//*************************************************************************************************************

void GameData::setWorldHeight(float worldHeight) {
	this->worldHeight = worldHeight;
}

//*************************************************************************************************************
//*************************************************************************************************************

void GameData::setBackGroundColor(const string& backgroundColor) {
	this->backgroundColor = backgroundColor;
}

//*************************************************************************************************************
//*************************************************************************************************************

const Turns* const GameData::getTurnsPtr() {
	return &turns;
}

//*************************************************************************************************************
//*************************************************************************************************************

void GameData::initTurns(int gameTurnsCount) {
	turns.resize(gameTurnsCount);
}

//*************************************************************************************************************
//*************************************************************************************************************

Turn* GameData::getTurnPtr(int gameTurnIdx) {
	return turns.data() + gameTurnIdx;
}
