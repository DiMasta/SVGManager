#include "game_data.h"

GameData::GameData() :
	turns(0)
{

}

//*************************************************************************************************************
//*************************************************************************************************************

GameData::~GameData() {
	turns.clear();
}

//*************************************************************************************************************
//*************************************************************************************************************

void GameData::initTurns(int gameTurnsCount) {
	turns.reserve(gameTurnsCount);
}

//*************************************************************************************************************
//*************************************************************************************************************

Turn* GameData::getTurnPtr(int gameTurnIdx) {
	return &(*(turns.begin() + gameTurnIdx));
}
